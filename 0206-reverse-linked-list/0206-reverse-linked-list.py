# Definition for singly-linked list.
# class ListNode(object):
#     def __init__(self, val=0, next=None):
#         self.val = val
#         self.next = next
class Solution(object):
    def reverseList(self, head):
        """
        :type head: Optional[ListNode]
        :rtype: Optional[ListNode]
        """
        prev = None
        cursor = head

        while cursor:
            nextptr = cursor.next
            cursor.next = prev
            prev = cursor
            cursor = nextptr

        return prev